const normalizeName = (value) =>
  String(value || '')
    .replace(/\s+/g, ' ')
    .replace(/[\u2018\u2019]/g, "'")
    .replace(/[\u2013\u2014]/g, '-')
    .trim()

const toPrice = (value) => {
  const parsed = Number.parseFloat(String(value).replace(/,/g, ''))
  if (!Number.isFinite(parsed) || parsed <= 0) {
    return null
  }
  return Math.round(parsed * 100) / 100
}

const mapUnit = (token) => {
  const normalized = String(token || '').toLowerCase()
  if (normalized.includes('tab')) return 'tablet'
  if (normalized.includes('cap')) return 'capsule'
  if (normalized.includes('syr')) return 'syrup'
  if (normalized.includes('inj')) return 'vial'
  if (normalized.includes('cream')) return 'cream'
  if (normalized.includes('susp')) return 'suspension'
  if (normalized.includes('drop')) return 'drops'
  if (normalized.includes('supp')) return 'suppository'
  if (normalized.includes('bottle')) return 'bottle'
  if (normalized.includes('pack')) return 'pack'
  if (normalized.includes('unit')) return 'unit'
  return 'unit'
}

const parseMedicineListText = (text) => {
  const lines = text.split(/\r?\n/).map((line) => line.trim()).filter(Boolean)
  const items = []

  for (const line of lines) {
    if (!/^\d+\s+/.test(line)) continue

    const priceMatch = line.match(/(\d+(?:\.\d+)?)\s*$/)
    if (!priceMatch) continue

    const price = toPrice(priceMatch[1])
    if (!price) continue

    const withoutPrice = line.slice(0, priceMatch.index).trim()
    const markerIndex = withoutPrice.search(/\s(?:Generic|Brand)\s/i)
    if (markerIndex === -1) continue

    let left = withoutPrice.slice(0, markerIndex).trim()
    left = left.replace(/^\d+\s+/, '')
    left = left.replace(/^\d+\s+/, '')
    left = left.replace(/^\d+\s+/, '')

    const name = normalizeName(left)
    if (!name || name.length < 3) continue

    const right = withoutPrice.slice(markerIndex).trim()
    const unitToken = (right.match(/\b(TAB|CAP|SYRUP|INJ|CREAM|SUSP|DROP|SUPP)\b/i) || [])[1] || 'unit'

    items.push({
      name,
      price,
      unit: mapUnit(unitToken),
      source: 'medicine list',
    })
  }

  return items
}

const parseMedicinePricingText = (text) => {
  const lines = text.split(/\r?\n/).map((line) => line.trim()).filter(Boolean)
  const items = []

  for (const line of lines) {
    if (/^healthbuk/i.test(line)) continue
    if (/^medication\s+cash/i.test(line)) continue

    const match = line.match(/^(.*?)\s+([0-9]+(?:\.[0-9]+)?)\s+([0-9]+(?:\.[0-9]+)?)\s+([0-9]+(?:\.[0-9]+)?)\s+([0-9]+(?:\.[0-9]+)?)\s+([A-Za-z]+)$/)
    if (!match) continue

    const name = normalizeName(match[1])
    const cashPrice = toPrice(match[2])
    const unit = mapUnit(match[6])

    if (!name || !cashPrice || name.length < 3) continue

    items.push({
      name,
      price: cashPrice,
      unit,
      source: 'medicine pricing list',
    })
  }

  return items
}

const uniqueByName = (records) => {
  const map = new Map()

  for (const record of records) {
    const key = record.name.toLowerCase()
    if (!map.has(key)) {
      map.set(key, record)
      continue
    }

    const existing = map.get(key)
    if (record.price > existing.price) {
      map.set(key, record)
    }
  }

  return Array.from(map.values())
}

const escapeSql = (value) => String(value).replace(/'/g, "''")

const buildDrugRows = (records) => {
  const expiryDate = '2028-12-31'
  return records.map((record, index) => ({
    name: record.name,
    batch_number: `PDF-IMP-${String(index + 1).padStart(5, '0')}`,
    expiry_date: expiryDate,
    quantity: 0,
    unit: record.unit || 'unit',
    price: record.price,
    cost_price: Math.max(0, Math.round(record.price * 0.8 * 100) / 100),
    supplier: 'PDF Import',
    category: null,
    description: `Imported from ${record.source} on ${new Date().toISOString()}`,
    reorder_level: 10,
    status: 'active',
  }))
}

const buildInsertSql = (rows) => {
  const header = `-- Generated by scripts/import-medicines-from-pdfs.mjs\n-- ${new Date().toISOString()}\n\n`
  if (!rows.length) return `${header}-- No rows generated.\n`

  const chunks = []
  const chunkSize = 500

  for (let i = 0; i < rows.length; i += chunkSize) {
    const chunk = rows.slice(i, i + chunkSize)
    const values = chunk
      .map(
        (r) => `('${escapeSql(r.name)}','${escapeSql(r.batch_number)}','${r.expiry_date}',${r.quantity},'${escapeSql(r.unit)}',${r.price},${r.cost_price},'${escapeSql(r.supplier)}',${r.category === null ? 'NULL' : `'${escapeSql(r.category)}'`},'${escapeSql(r.description)}',${r.reorder_level},'${r.status}')`
      )
      .join(',\n')

    chunks.push(`INSERT INTO drugs (name, batch_number, expiry_date, quantity, unit, price, cost_price, supplier, category, description, reorder_level, status)\nVALUES\n${values}\nON CONFLICT (name, batch_number) DO UPDATE SET\n  price = EXCLUDED.price,\n  cost_price = EXCLUDED.cost_price,\n  updated_at = NOW();\n`)
  }

  return header + chunks.join('\n')
}

const readEnvFile = async () => {
  const envPath = path.join(projectRoot, '.env')
  const content = await fs.readFile(envPath, 'utf8')
  const env = {}

  for (const rawLine of content.split(/\r?\n/)) {
    const line = rawLine.trim()
    if (!line || line.startsWith('#')) continue
    const idx = line.indexOf('=')
    if (idx <= 0) continue
    const key = line.slice(0, idx).trim()
    const value = line.slice(idx + 1).trim()
    env[key] = value
  }

  return env
}

const importToSupabase = async (rows) => {
  const env = await readEnvFile()
  const supabaseUrl = env.VITE_SUPABASE_URL
  const serviceRoleKey = env.SUPABASE_SERVICE_ROLE_KEY

  if (!supabaseUrl || !serviceRoleKey) {
    return {
      imported: 0,
      skipped: rows.length,
      mode: 'sql-only',
      reason: 'SUPABASE_SERVICE_ROLE_KEY not found in .env',
    }
  }

  const supabase = createClient(supabaseUrl, serviceRoleKey, {
    auth: { persistSession: false, autoRefreshToken: false },
  })

  const chunkSize = 250
  let imported = 0

  for (let i = 0; i < rows.length; i += chunkSize) {
    const chunk = rows.slice(i, i + chunkSize)
    const { error } = await supabase
      .from('drugs')
      .upsert(chunk, { onConflict: 'name,batch_number' })

    if (error) {
      throw new Error(`Failed at chunk ${i / chunkSize + 1}: ${error.message}`)
    }

    imported += chunk.length
  }

  return {
    imported,
    skipped: 0,
    mode: 'direct-import',
    reason: null,
  }
}

const readPdfText = async (pdfPath) => {
  const buffer = await fs.readFile(pdfPath)
  const parser = new PDFParse({ data: buffer })
  const result = await parser.getText()
  await parser.destroy()
  return result.text || ''
}

const main = async () => {
  await fs.mkdir(outputDir, { recursive: true })

  const allParsedItems = []
  const sourceStats = []

  for (const source of sourceFiles) {
    const text = await readPdfText(source.filePath)
    const parsed = source.label === 'medicine list'
      ? parseMedicineListText(text)
      : parseMedicinePricingText(text)

    sourceStats.push({ source: source.label, parsed: parsed.length })
    allParsedItems.push(...parsed)
  }

  const uniqueItems = uniqueByName(allParsedItems)
  const rows = buildDrugRows(uniqueItems)

  const jsonPath = path.join(outputDir, 'medicines-import.json')
  const sqlPath = path.join(outputDir, 'medicines-import.sql')

  await fs.writeFile(jsonPath, JSON.stringify(rows, null, 2), 'utf8')
  await fs.writeFile(sqlPath, buildInsertSql(rows), 'utf8')

  const importResult = await importToSupabase(rows)

  const summary = {
    generatedAt: new Date().toISOString(),
    sources: sourceStats,
    totalParsed: allParsedItems.length,
    uniqueMedicines: rows.length,
    output: {
      jsonPath,
      sqlPath,
    },
    import: importResult,
  }

  console.log(JSON.stringify(summary, null, 2))
}

main().catch((error) => {
  console.error(error.message)
  process.exit(1)
})
